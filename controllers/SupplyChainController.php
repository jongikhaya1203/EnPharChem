<?php
/**
 * EnPharChem - Supply Chain Management Controller
 * Supply chain optimization and management modules
 */

class SupplyChainController extends BaseController {

    private $categorySlug = 'supply-chain-mgmt';

    public function index() {
        $category = $this->db->fetch(
            "SELECT * FROM module_categories WHERE slug = ?",
            [$this->categorySlug]
        );

        $modules = $this->getModulesByCategory($this->categorySlug);

        $this->view('modules/category', [
            'pageTitle' => 'Supply Chain Management',
            'category' => $category,
            'modules' => $modules,
        ]);
    }

    public function module() {
        $slug = $_GET['slug'] ?? '';
        if (empty($slug)) {
            $this->redirect('supply-chain-mgmt');
        }

        $module = $this->getModule($slug);
        if (!$module) {
            $this->redirect('supply-chain-mgmt');
        }

        $projects = $this->db->fetchAll(
            "SELECT * FROM projects WHERE user_id = ? ORDER BY updated_at DESC",
            [$this->user['id']]
        );

        $this->view('modules/workspace', [
            'pageTitle' => $module['name'],
            'module' => $module,
            'projects' => $projects,
        ]);
    }
}
